package com.auxesis.maxcrowdfund.mvvm.ui.homeDetail.model;

public class RiskModel {
    String mRiskTitle;
    String mRiskValue;
    String mScoreTitle;
    String mScoreValue;
    String mMeasureTitle;
    String mMeasureValue;


    public RiskModel() {
    }

    public String getmRiskTitle() {
        return mRiskTitle;
    }

    public void setmRiskTitle(String mRiskTitle) {
        this.mRiskTitle = mRiskTitle;
    }

    public String getmRiskValue() {
        return mRiskValue;
    }

    public void setmRiskValue(String mRiskValue) {
        this.mRiskValue = mRiskValue;
    }

    public String getmScoreTitle() {
        return mScoreTitle;
    }

    public void setmScoreTitle(String mScoreTitle) {
        this.mScoreTitle = mScoreTitle;
    }

    public String getmScoreValue() {
        return mScoreValue;
    }

    public void setmScoreValue(String mScoreValue) {
        this.mScoreValue = mScoreValue;
    }

    public String getmMeasureTitle() {
        return mMeasureTitle;
    }

    public void setmMeasureTitle(String mMeasureTitle) {
        this.mMeasureTitle = mMeasureTitle;
    }

    public String getmMeasureValue() {
        return mMeasureValue;
    }

    public void setmMeasureValue(String mMeasureValue) {
        this.mMeasureValue = mMeasureValue;
    }
}
